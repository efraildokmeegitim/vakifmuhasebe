    private void btnSilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSilActionPerformed
        try {
            int row=tblKasalar.getSelectedRow();
        int id=Integer.parseInt(tblKasalar.getValueAt(row, 0).toString());
        KasalarDao kdao=new KasalarDao();
        kdao.kasaSil(id);
        JOptionPane.showMessageDialog(rootPane, "Kasa Silindi");
        tabloDoldur();
        } catch (Exception e) {
            System.out.println("Silme İşlemi Sırasında Hata Oluştu."+e);
        }
        
    }//GEN-LAST:event_btnSilActionPerformed

    private void tblKasalarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKasalarMouseClicked
            // TODO add your handling code here:
            kasa=new KasalarEntity();
            selectedRow=tblKasalar.getSelectedRow();
            
            kasa.setId(Integer.parseInt(tblKasalar.getValueAt(selectedRow, 0).toString()));
            kasa.setKasaAdi( tblKasalar.getValueAt(selectedRow, 1).toString()  );
            kasa.setParaBirimi(tblKasalar.getValueAt(selectedRow, 2).toString());
            kasa.setAciklama(tblKasalar.getValueAt(selectedRow, 3).toString() );
            txtKasaAdi.setText(kasa.getKasaAdi());
            txtParaBirimi.setText(kasa.getParaBirimi());
            txtAciklama.setText(kasa.getAciklama());
            
            
            
    }//GEN-LAST:event_tblKasalarMouseClicked

