     private void tabloDoldur(){
        try {
            KasalarDao kdao=new KasalarDao();
            ResultSet rs=kdao.tumKasalar();
                // create a table model with the appropriate column headers
                // and with 0 rows (to start with)
                tblKasalar.removeAll();
                String[] columnNames = {"id","Kasa Adı", "Para Birimi", "Açıklama"};
                DefaultTableModel tableModel = new DefaultTableModel(columnNames, 0);

                while (rs.next()) {
                    String id=Integer.toString(rs.getInt("id"));
                    String kasaAdi = rs.getString("kasaAdi");
                    String paraBirimi = rs.getString("paraBirimi");
                    String aciklama = rs.getString("aciklama");

                    // create a single array of one row's worth of data
                    String[] data = { id,kasaAdi, paraBirimi, aciklama } ;

                    // and add this row of data into the table model
                    tableModel.addRow(data);
                }

                tblKasalar.setModel(tableModel); // place model into JTable

                 
                
            } catch (SQLException ex) {
                Logger.getLogger(FrmKasalar.class.getName()).log(Level.SEVERE, null, ex);
            }
    
    }
