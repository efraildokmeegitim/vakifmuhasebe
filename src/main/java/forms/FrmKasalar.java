    private void btnGuncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuncelleActionPerformed
        // TODO add your handling code here:
        if (txtKasaAdi.getText() !="" || txtParaBirimi.getText()!="") {
            kasa.setKasaAdi(txtKasaAdi.getText());
            kasa.setParaBirimi(txtParaBirimi.getText());
            kasa.setAciklama(txtAciklama.getText());
            KasalarDao kdao=new KasalarDao();
            kdao.kasaGuncelle(kasa);
            JOptionPane.showMessageDialog(rootPane, "Kasa Güncellendi.");
            temizle();
            tabloDoldur();
        }
    }//GEN-LAST:event_btnGuncelleActionPerformed

